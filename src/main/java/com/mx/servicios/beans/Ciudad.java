public class Ciudad {
  String idCiudad;
  String descripcion;

  public String getIdCiudad() {
    return idCiudad;
  }

  public void setIdCiudad(String idCiudad) {
    this.idCiudad = idCiudad;
  }

  public String getDescripcion() {
    return descripcion;
  }

  public void setDescripcion(String descripcion) {
    this.descripcion = descripcion;
  }
}
